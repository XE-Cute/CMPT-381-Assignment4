package com.a4basics;

public class SelectionRectangle {
    public double originX, originY, x, y, width, height;

    public SelectionRectangle(double newX, double newY, double newWidth, double newHeight){
        originX = newX;
        originY = newY;
        x = newX;
        y = newY;
        width = newWidth;
        height = newHeight;
    }
}
